def print_rgb(message, r, g, b):
    print(f'\033[38;2;{r};{g};{b}m' + message + '\033[0m')

def is_prime(n):
    if n <= 1:
        return False
    for i in range(2, n):
        if n % i == 0:
            return False
    return True

count = 0
n = 2
while count < 8:
    if is_prime(n):
        print_rgb(str(n), 73, 49, 91)
        count += 1
    n += 1
